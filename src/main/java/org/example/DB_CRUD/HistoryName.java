package org.example.DB_CRUD;

public class HistoryName {

    String H_ID;
    String MY_X;
    String MY_Y;
    String HISTORY_DATE;

    public String getH_ID() {
        return H_ID;
    }

    public void setH_ID(String h_ID) {
        H_ID = h_ID;
    }

    public String getMY_X() {
        return MY_X;
    }

    public void setMY_X(String MY_X) {
        this.MY_X = MY_X;
    }

    public String getMY_Y() {
        return MY_Y;
    }

    public void setMY_Y(String MY_Y) {
        this.MY_Y = MY_Y;
    }

    public String getHISTORY_DATE() {
        return HISTORY_DATE;
    }

    public void setHISTORY_DATE(String HISTORY_DATE) {
        this.HISTORY_DATE = HISTORY_DATE;
    }
}
